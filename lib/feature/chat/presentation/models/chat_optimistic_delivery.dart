/// Доставка исходящего сообщения при оптимистичной отправке текста.
enum ChatOptimisticDelivery {
  /// Ждём ответ RPC (показываем индикатор).
  sending,

  /// Сервер принял сообщение; ждём совпадения со строкой из списка.
  ack,

  /// Совпало с записью из [ChatThreadItem.optimisticText.server].
  synced,

  /// Ошибка отправки (можно повторить).
  failed,
}
